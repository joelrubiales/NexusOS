#include "nexus.h"

#define VGA_MISC_WRITE  0x3C2
#define VGA_SEQ_INDEX   0x3C4
#define VGA_SEQ_DATA    0x3C5
#define VGA_CRTC_INDEX  0x3D4
#define VGA_CRTC_DATA   0x3D5
#define VGA_GC_INDEX    0x3CE
#define VGA_GC_DATA     0x3CF
#define VGA_AC_INDEX    0x3C0
#define VGA_AC_WRITE    0x3C0
#define VGA_INSTAT_READ 0x3DA

static inline void vga_write_reg(unsigned short ip, unsigned short dp,
                                 unsigned char idx, unsigned char val) {
    outb(ip, idx);
    outb(dp, val);
}

static inline unsigned char vga_read_reg(unsigned short ip, unsigned short dp,
                                         unsigned char idx) {
    outb(ip, idx);
    return inb(dp);
}

static void vga_write_regs(const unsigned char* r) {
    outb(VGA_MISC_WRITE, r[0]);

    for(unsigned char i = 0; i < 5; i++)
        vga_write_reg(VGA_SEQ_INDEX, VGA_SEQ_DATA, i, r[1+i]);

    vga_write_reg(VGA_CRTC_INDEX, VGA_CRTC_DATA, 0x03,
        vga_read_reg(VGA_CRTC_INDEX, VGA_CRTC_DATA, 0x03) | 0x80);
    vga_write_reg(VGA_CRTC_INDEX, VGA_CRTC_DATA, 0x11,
        vga_read_reg(VGA_CRTC_INDEX, VGA_CRTC_DATA, 0x11) & ~0x80);

    for(unsigned char i = 0; i < 25; i++)
        vga_write_reg(VGA_CRTC_INDEX, VGA_CRTC_DATA, i, r[6+i]);

    for(unsigned char i = 0; i < 9; i++)
        vga_write_reg(VGA_GC_INDEX, VGA_GC_DATA, i, r[31+i]);

    for(unsigned char i = 0; i < 21; i++) {
        (void)inb(VGA_INSTAT_READ);
        outb(VGA_AC_INDEX, i);
        outb(VGA_AC_WRITE, r[40+i]);
    }

    (void)inb(VGA_INSTAT_READ);
    outb(VGA_AC_INDEX, 0x20);
}

