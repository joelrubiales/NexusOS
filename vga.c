void vga_set_mode_320x200x256_linear(void) {
    vga_write_regs(mode13_regs);

    unsigned char sm = vga_read_reg(VGA_SEQ_INDEX, VGA_SEQ_DATA, 0x04);
    sm |= 0x08;
    sm &= (unsigned char)~0x04;
    vga_write_reg(VGA_SEQ_INDEX, VGA_SEQ_DATA, 0x04, sm);

    vga_write_reg(VGA_GC_INDEX, VGA_GC_DATA, 0x05, 0x40);
    vga_write_reg(VGA_GC_INDEX, VGA_GC_DATA, 0x06, 0x05);

    volatile unsigned char* vr = (volatile unsigned char*)0xA0000;
    for(int i = 0; i < 64000; i++) vr[i] = 0;
}

void vga_set_text_80x25(void) {
    vga_write_regs(text_regs);
}
